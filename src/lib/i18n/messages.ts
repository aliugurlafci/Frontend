/** All locale dictionaries. EN is the source of truth; TR/DE must mirror its keys
 *  (enforced by tests/i18n.test.ts so a missing translation fails CI, not silently
 *  falls back to English at runtime). */
export const MESSAGES: Record<Locale, Dict> = { en, tr, de };

/** Translate a UI key for a locale, with {var} interpolation; falls back to EN then the key. */
export function t(locale: Locale, key: string, vars?: Record<string, string>): string {
  const dict = MESSAGES[locale] ?? MESSAGES[DEFAULT_LOCALE];
  let str = dict[key] ?? MESSAGES.en[key] ?? key;
  if (vars) for (const [k, v] of Object.entries(vars)) str = str.replaceAll(`{${k}}`, v);
  return str;
}
